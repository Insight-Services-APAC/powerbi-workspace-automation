/****** Script for SelectTopNRows command from SSMS  ******/
CREATE VIEW CEN.vw_Datasets
AS
SELECT [Dataset_Id]
      ,[Name]
      ,[ConfiguredBy]
      ,[AddRowsAPIEnabled]
      ,[WebUrl]
      ,[IsRefreshable]
      ,[IsEffectiveIdentityRequired]
      ,[IsEffectiveIdentityRolesRequired]
      ,[IsOnPremGatewayRequired]
      ,[Encryption]
      ,[CreatedDate]
      ,[ContentProviderType]
      ,[CreateReportEmbedURL]
      ,[QnaEmbedURL]
      ,[Description]
      ,[EndorsementDetails]
      ,[DatasourceUsages]
      ,[UpstreamDataflows]
      ,[SensitivityLabel]
      ,[Workspace_Id]
      ,[DateRetrieved]
  FROM [CEN].[Datasets]